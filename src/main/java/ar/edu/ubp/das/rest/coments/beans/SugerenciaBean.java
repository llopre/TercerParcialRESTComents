package ar.edu.ubp.das.rest.coments.beans;

public class SugerenciaBean {
	
	
	private String codTipoServicio;
	private int nroTema;
	private String sugerencia;
	public String getCodTipoServicio() {
		return codTipoServicio;
	}
	public void setCodTipoServicio(String codTipoServicio) {
		this.codTipoServicio = codTipoServicio;
	}
	public int getNroTema() {
		return nroTema;
	}
	public void setNroTema(int nroTema) {
		this.nroTema = nroTema;
	}
	public String getSugerencia() {
		return sugerencia;
	}
	public void setSugerencia(String sugerencia) {
		this.sugerencia = sugerencia;
	}
	


}
